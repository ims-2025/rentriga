/* ===== i18n helpers ===== */
window.RR_getLang = function(){
  return localStorage.getItem("rr_lang") || "en";
};
window.RR_setLang = function(lang){
  localStorage.setItem("rr_lang", lang);
  document.documentElement.setAttribute("lang", lang);
  window.RR_applyI18n();
};
window.RR_t = function(key){
  const lang = window.RR_getLang();
  return (window.RR_I18N[lang] && window.RR_I18N[lang][key]) || window.RR_I18N.en[key] || key;
};
window.RR_applyI18n = function(){
  document.querySelectorAll("[data-t]").forEach(el=>{
    const k = el.getAttribute("data-t");
    el.textContent = window.RR_t(k);
  });
  document.querySelectorAll("[data-t-ph]").forEach(el=>{
    const k = el.getAttribute("data-t-ph");
    el.setAttribute("placeholder", window.RR_t(k));
  });
  document.querySelectorAll(".lang-switch button").forEach(b=>{
    b.classList.toggle("active", b.dataset.lang === window.RR_getLang());
  });
  /* Re-render any data-driven views */
  if (typeof window.RR_renderAll === "function") window.RR_renderAll();
};
